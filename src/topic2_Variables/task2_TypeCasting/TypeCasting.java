package topic2_Variables.task2_TypeCasting;

public class TypeCasting {
    public static void main(String[] args) {
        int i = 500;
        double castI = (double) i;
        System.out.println("Значение переменной типа int: " + i);
        System.out.println("Значение переменной типа double: " + castI);
    }
}
